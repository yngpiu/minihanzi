window.addEventListener('load', function () {
  function searchHandler() {
    $('.search-results .results').remove();
    var key = $('#searchInput').val();
    if ($.trim(key)) {
      key = key.toLowerCase();
      var lis = [];
      var f1 = [];
      var f2 = [];
      var f3 = [];
      for (let index = 0; index < pinyinTable.length; index++) {
        f1 = f1.concat(
          pinyinTable[index].filter((item) => {
            return (
              item.text[0] == key.replace('v', 'ü') ||
              item.text[0] == key.replace('v', 'u')
            );
          }),
        );
        f2 = f2.concat(
          pinyinTable[index].filter((item) => {
            return (
              item.text[0].length != key.length &&
              (item.text[0].indexOf(key.replace('v', 'ü')) > -1 ||
                item.text[0].indexOf(key.replace('v', 'u')) > -1)
            );
          }),
        );
      }
      f1.concat(f2)
        .concat(f3)
        .forEach((element) => {
          lis.push(`<li class="results">${element.text[0]}</li>`);
        });
      if (lis.length == 0)
        $('.search-results').html(
          '<li class="results-alert">Sorry, we couldn’t find it! Please check your spelling and try again!</li>',
        );
      else $('.search-results').html(lis.join(''));
      $('.search-results').show();
    }
  }

  function createdPinyinTable() {
    var pinyinChart = $('#pinyinChart');
    var trHtml = [];
    for (let index = 0; index < pinyinTable.length; index++) {
      const tr = pinyinTable[index];
      var tdHtml = '';
      for (let tdindex = 0; tdindex < tr.length; tdindex++) {
        const { text, isNull } = tr[tdindex];
        tdHtml += `<td class="${tdindex == 0 ? 'td-cell' : ''} td-pinyin__${
          text[0]
        } ${!isNull || 'null'}" 
              data-index="[${index},${tdindex}]"
            >${text[0]}</td>`;
      }
      trHtml.push(
        `<tr class="${index == 0 ? 'tr-header' : ''}">${tdHtml}</tr>`,
      );
    }
    pinyinChart.html(trHtml.join(''));
    $('#pinyinChart').on('click', 'td:not(.null)', function (e) {
      e.stopPropagation();
      closePop();
      var target = $(e.target);
      const client = target[0].getBoundingClientRect();
      if (client.top + 300 > document.scrollingElement.clientHeight)
        $(document.scrollingElement)
          .stop()
          .animate({
            scrollTop:
              document.scrollingElement.scrollTop +
              target[0].getBoundingClientRect().top -
              document.scrollingElement.clientHeight / 2,
          });
      const tableContainer = $('.pinyin-chart-table');
      if (
        client.left < 0 ||
        client.left >
          tableContainer[0].clientWidth + tableContainer[0].scrollLeft
      )
        tableContainer.stop().animate(
          {
            scrollLeft:
              tableContainer[0].scrollLeft +
              (client.left - tableContainer[0].clientWidth / 2),
          },
          300,
        );
      target.addClass('active');
      var { index } = target.data();
      var data = pinyinTable[index[0]][index[1]];
      $('.pinyin_var').text(data.text[0]);
      $('.pinyin_join').text(data.join);
      $('.tones').html(
        data.text.map((text, tindex) => {
          return data.audio[tindex]
            ? `
            <button class="yy-button yy-button--text btn-play-audio" data-url="${data.audio[tindex]}">${text}
              <img class="play-button" src="/public/images/tools/icon_audio_play.webp" width='16px'> <audio src='${data.audio[tindex]}' preload="auto"/>
            </button>
            `
            : '';
        }),
      );

      if (data.filename) {
        const baseUri =
          'https://video.yoyochinese.com/s/pinyin-chart/' + data.filename;
        $('.pinyin-video-container').show();
        const videoElement = $('.pinyin-video-container video');

        if (videoElement.attr('data-filename') !== data.filename) {
          videoElement.replaceWith(
            '<video' +
              ' data-filename="' +
              data.filename +
              '"' +
              ' class="main-iframe"' +
              ' disablepictureinpicture' +
              ' preload="none"' +
              ' crossorigin="anonymous"' +
              ' playsinline' +
              ' controls' +
              ' controlsList="nodownload"' +
              ' poster="' +
              'https://cdn.yoyochinese.com/thumbnails/' +
              data.originalId +
              '.webp"' +
              '>' +
              '<source src="' +
              baseUri +
              '.mp4" type="video/mp4" />' +
              '<source src="' +
              baseUri +
              '.webm" type="video/webm" />' +
              '</video>',
          );
        }
      } else $('.pinyin-video-container').hide();

      const popover = $('.popover');
      popover.show();

      var offset = target.offset();
      var left = (left = offset.left + e.target.offsetWidth);
      if (document.body.clientWidth < left + popover[0].clientWidth) {
        left = offset.left - popover[0].clientWidth;
      }
      if (left < 0 || left > document.body.clientWidth) {
        popover.css({ left: 12 });
        setPopoverTop(offset.top + 24);
      } else {
        popover.css({ left });
        setPopoverTop(offset.top);
      }
      setPopoverTop();
    });
    $('.popover .close-pinyin-pop').click(function () {
      closePop();
    });
  }
  function setPopoverTop(top) {
    $('.popover').attr('top', top);
    $('.popover').css({ top: top - document.scrollingElement.scrollTop });
  }
  function closePop() {
    document.querySelector('.pinyin-video-container video').pause();
    $('td.active').removeClass('active');
    $('.popover').hide();
  }
  createdPinyinTable();

  function registEvent() {
    window.addEventListener('scroll', () => {
      setPopoverTop($('.popover').attr('top'));
    });

    $('.search-results').on('click', '.results', function (e) {
      e.stopPropagation();
      $(`.td-pinyin__${$(e.target).text()}`).trigger('click');
      $('.search-results').hide();
    });

    $('#searchInput').on('input click', function (e) {
      e.stopPropagation();
      searchHandler();
    });
    $('#searchInput').click((e) => e.stopPropagation());
    $('#btn_Proit').click(function (e) {
      e.stopPropagation();
      $('.search-results li:first-child').trigger('click');
    });
    $(document).on('click', '.btn-play-audio', function (e) {
      e.stopPropagation();
      $(e.currentTarget).find('audio')[0].play();
    });

    document.addEventListener('click', function (e) {
      if ($(e.target).closest('.popover').length) return;
      $('.search-results').hide();
      closePop();
    });
  }

  registEvent();
});
