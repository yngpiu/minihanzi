const TONE_MARKS: Record<string, [string, string, string, string, string]> = {
	a: ["ā", "á", "ǎ", "à", "a"],
	e: ["ē", "é", "ě", "è", "e"],
	i: ["ī", "í", "ǐ", "ì", "i"],
	o: ["ō", "ó", "ǒ", "ò", "o"],
	u: ["ū", "ú", "ǔ", "ù", "u"],
	v: ["ǖ", "ǘ", "ǚ", "ǜ", "ü"],
	ü: ["ǖ", "ǘ", "ǚ", "ǜ", "ü"],
	m: ["m̄", "ḿ", "m̌", "m̀", "m"],
	n: ["n̄", "ń", "ň", "ǹ", "n"],
};

function isValidSyllable(s: string): boolean {
	return SYLLABLES.has(s);
}

function addToneMark(syllable: string, tone: number): string {
	const lower = syllable.toLowerCase();

	let vowelToMark = "";
	let pos = -1;

	for (let i = 0; i < lower.length; i++) {
		const ch = lower[i];
		if (ch === "a" || ch === "e" || ch === "o") {
			vowelToMark = ch;
			pos = i;
			break;
		}
		if (
			ch === "i" ||
			ch === "u" ||
			ch === "v" ||
			ch === "ü" ||
			ch === "m" ||
			ch === "n"
		) {
			vowelToMark = ch;
			pos = i;
		}
	}

	if (pos < 0 || !vowelToMark) return syllable;

	const marks = TONE_MARKS[vowelToMark];
	if (!marks) return syllable;

	const originalChar = syllable[pos];
	const isUpper = originalChar !== originalChar.toLowerCase();
	const marked = isUpper ? marks[tone - 1].toUpperCase() : marks[tone - 1];

	return syllable.slice(0, pos) + marked + syllable.slice(pos + 1);
}

function normalizeSyllable(s: string): string {
	return s.toLowerCase().replace(/:/g, "").replace("u:", "v").replace("ü", "v");
}

export function applyPinyinIME(value: string): string {
	// v is only used as ü substitute in pinyin — convert immediately
	const withUmlaut = value.replace(/v/g, "ü").replace(/V/g, "Ü");

	const digitMatch = withUmlaut.match(/([1-5])$/);
	if (!digitMatch) return withUmlaut;

	const tone = parseInt(digitMatch[1], 10);
	const text = withUmlaut.slice(0, -1);

	const lettersMatch = text.match(/([a-zA-ZüÜvV:]+)$/);
	if (!lettersMatch) return value;

	const rawSyllable = lettersMatch[1];
	const normalized = normalizeSyllable(rawSyllable);

	if (!isValidSyllable(normalized)) return value;

	const toned = addToneMark(rawSyllable, tone);
	const cleaned = toned.replace(/:/g, "");
	const before = text.slice(0, -rawSyllable.length);
	return before + cleaned;
}
